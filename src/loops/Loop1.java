package loops;

public class Loop1 {
    public static void main(String[] args) {

        for (int i = 0;i<=5; i++){
            System.out.print(i++);



            //                                                   FOR


        }
        for (int i = 10;i >=0;i--){
            System.out.println(i);


        }
        String color = "Black";
        for (int i = 0; i  <  color.length();i++){
            System.out.println(color.charAt(i));
        }

        for (int k = 0; k<20 ;k++){
            System.out.println(k+"* 2=" +k*2);
        }

    }

}