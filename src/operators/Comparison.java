package operators;

public class Comparison {
    public static void main(String[] args) {

        System.out.println(89 > 34); //true
        System.out.println(34 > 89); //true
        System.out.println(1 > 15); //false

        System.out.println(15 == 15); // true
        System.out.println(15 != 15); // false

        System.out.println(40 >= 20); //true
        System.out.println(15 <= 15);


    }

}
